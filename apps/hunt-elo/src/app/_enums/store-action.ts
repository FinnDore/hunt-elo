/**
 * Actions for the global store
 */
export enum StoreAction {
    /**
     * Sets dark or light mode
     */
    SET_THEME_MODE = 'SET_THEME_MODE',
    /**
     * Sets the current path
     */
    SET_PATH = 'SET_PATH',
    /**
     * Appends an elo value for a given player
     */
    APPEND_ELO = 'APPEND_ELO',
}
